export function generateObject(
  start: number = new Date(2020, 6, 1).getTime(),
  end: number = new Date(2022, 11, 31).getTime(),
  isWeekDaysOnly: boolean = false
): Record<string, any>[] {
  const data: Record<string, any>[] = [];
  const names: string[] = [
    'Story Time for Kids',
    'Camping with Turtles',
    'Wildlife Warriors',
    'Parrot Talk',
    'Birds of Prey',
    'Croco World',
    'Venomous Snake Hunt',
    'Face Painting & Drawing events',
    'Pony Rides',
    'Feed the Giants',
    'Jungle Treasure Hunt',
    'Endangered Species Program',
    'Black Cockatoos Playtime',
    'Walk with Jungle King',
    'Trained Climbers',
    'Playtime with Chimpanzees',
    'Meet a small Mammal',
    'Amazon Fish Feeding',
    'Elephant Ride',
  ];
  const dayCount: number = 1000 * 60 * 60;
  const appCount: number = isWeekDaysOnly ? 1 : 9;
  for (let a: number = start, id = 1; a < end; a += dayCount * 24) {
    const count: number = Math.floor(Math.random() * appCount + 1);
    for (let b = 0; b < count; b++) {
      const hour: number = Math.floor(Math.random() * 100) % 24;
      const minutes: number =
        Math.round((Math.floor(Math.random() * 100) % 60) / 5) * 5;
      const nCount: number = Math.floor(Math.random() * names.length);
      const startDate: Date = new Date(new Date(a).setHours(hour, minutes));
      const endDate: Date = new Date(startDate.getTime() + dayCount * 2.5);

      if (
        (isWeekDaysOnly && [0, 6].indexOf(startDate.getDay()) > -1) ||
        [0, 6].indexOf(endDate.getDay()) > -1
      ) {
        continue;
      }

      data.push({
        Id: id,
        Subject: names[nCount],
        StartTime: startDate,
        EndTime: endDate,
        IsAllDay: id % 10 ? false : true,
      });
      id++;
    }
  }
  return data;
}

